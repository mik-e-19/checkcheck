int	ft_strcmp(char *s1, char *s2)
{
	unsigned char	c1;
	unsigned char	c2;
	int				idx;

	idx = -1;
	while (s1[++idx] != '\0' || s2[idx] != '\0')
	{
		c1 = s1[idx];
		c2 = s2[idx];
		if ((c1 - c2) != 0)
			return (c1 - c2);
	}
	return (0);
}
